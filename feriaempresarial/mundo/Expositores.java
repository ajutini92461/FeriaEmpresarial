package universidadean.feriaempresarial.mundo;

public class Expositores {

    private String Zona;
    private Integer NExpositores;

    public void setNExpositores(Integer nExpositores) {
        NExpositores = nExpositores;
    }
    public void setZona(String zona) {
        Zona = zona;
    }
    public Integer getNExpositores() {
        return NExpositores;
    }
    public String getZona() {
        return Zona;
    }
}
